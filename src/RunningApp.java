        setTitle("üèÉ‚Äç‚ôÇÔ∏è –ë–µ–≥ üèÉ‚Äç‚ôÄÔ∏è"); // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –æ–∫–Ω–∞
        setDefaultCloseOperation(EXIT_ON_CLOSE); // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –æ–ø–µ—Ä–∞—Ü–∏—é –∑–∞–∫—Ä—ã—Ç–∏—è –æ–∫–Ω–∞ –Ω–∞ –≤—ã—Ö–æ–¥ –∏–∑ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è
        pack(); // –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ä–∞–∑–º–µ—Ä –æ–∫–Ω–∞ —Ç–∞–∫, —á—Ç–æ–±—ã –æ–Ω —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–æ–≤–∞–ª —Ä–∞–∑–º–µ—Ä—É –µ–≥–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ
